// ── Modal & UI functions (global scope so onclick= works) ──

function openPoem(index) {
  const p = poems[index];
  document.getElementById('modal-title').textContent = p.title;
  document.getElementById('modal-label').textContent = p.label;
  document.getElementById('modal-body').textContent = p.body;
  document.getElementById('poem-modal').style.display = 'flex';
  document.body.style.overflow = 'hidden';
  document.getElementById('poem-modal-inner').scrollTop = 0;
}

function closePoem(event) {
  if (event && event.target !== document.getElementById('poem-modal')) return;
  document.getElementById('poem-modal').style.display = 'none';
  document.body.style.overflow = '';
}

function openEssay(id) {
  document.getElementById('essay-modal').style.display = 'flex';
  document.body.style.overflow = 'hidden';
  document.getElementById('essay-modal-inner').scrollTop = 0;
}

function closeEssay(event) {
  if (event.target !== document.getElementById('essay-modal')) return;
  document.getElementById('essay-modal').style.display = 'none';
  document.body.style.overflow = '';
}

function openReflection() {
  document.getElementById('reflection-modal').style.display = 'flex';
  document.body.style.overflow = 'hidden';
  document.getElementById('reflection-modal-inner').scrollTop = 0;
}

function closeReflection(event) {
  if (event.target !== document.getElementById('reflection-modal')) return;
  document.getElementById('reflection-modal').style.display = 'none';
  document.body.style.overflow = '';
}

// Escape key closes all modals
document.addEventListener('keydown', function(e) {
  if (e.key === 'Escape') {
    ['poem-modal','essay-modal','reflection-modal'].forEach(id => {
      const el = document.getElementById(id);
      if (el) el.style.display = 'none';
    });
    document.body.style.overflow = '';
  }
});

// ── Main init — runs after DOM is ready ──
document.addEventListener('DOMContentLoaded', () => {

  /* ─── NAVIGATION ─── */
  const nav = document.querySelector('.nav');
  const navLinks = document.querySelectorAll('.nav__link[data-page]');
  const pages = document.querySelectorAll('.page');
  const toggle = document.querySelector('.nav__toggle');
  const overlay = document.querySelector('.nav-overlay');
  const overlayClose = document.querySelector('.nav-overlay__close');
  const overlayLinks = document.querySelectorAll('.nav-overlay .nav__link[data-page]');

  window.addEventListener('scroll', () => {
    nav.classList.toggle('scrolled', window.scrollY > 20);
  }, { passive: true });

  function switchPage(pageId) {
    pages.forEach(p => p.classList.toggle('active', p.dataset.page === pageId));
    navLinks.forEach(l => l.classList.toggle('active', l.dataset.page === pageId));
    overlayLinks.forEach(l => l.classList.toggle('active', l.dataset.page === pageId));
    window.scrollTo({ top: 0, behavior: 'smooth' });
    overlay.classList.remove('open');
    document.body.style.overflow = '';
    setTimeout(initReveal, 100);
  }

  navLinks.forEach(link => link.addEventListener('click', () => switchPage(link.dataset.page)));
  overlayLinks.forEach(link => link.addEventListener('click', () => switchPage(link.dataset.page)));

  toggle.addEventListener('click', () => {
    overlay.classList.add('open');
    document.body.style.overflow = 'hidden';
  });
  overlayClose.addEventListener('click', () => {
    overlay.classList.remove('open');
    document.body.style.overflow = '';
  });

  document.addEventListener('click', e => {
    const btn = e.target.closest('[data-goto]');
    if (btn) switchPage(btn.dataset.goto);
  });

  /* ─── SCROLL REVEAL ─── */
  function initReveal() {
    const reveals = document.querySelectorAll('.reveal:not(.visible)');
    if (!reveals.length) return;
    const io = new IntersectionObserver(entries => {
      entries.forEach((entry, i) => {
        if (entry.isIntersecting) {
          setTimeout(() => entry.target.classList.add('visible'), i * 80);
          io.unobserve(entry.target);
        }
      });
    }, { threshold: 0.12 });
    reveals.forEach(el => io.observe(el));
  }
  initReveal();

  /* ─── ARCHIVE FILTER ─── */
  const pills = document.querySelectorAll('.pill');
  const archiveItems = document.querySelectorAll('.archive-item');
  pills.forEach(pill => {
    pill.addEventListener('click', () => {
      pills.forEach(p => p.classList.remove('active'));
      pill.classList.add('active');
      const filter = pill.dataset.filter;
      archiveItems.forEach(item => {
        item.style.display = (filter === 'all' || (item.dataset.tags || '').includes(filter)) ? '' : 'none';
      });
    });
  });

  /* ─── PROTECTION ─── */
  document.addEventListener('contextmenu', e => e.preventDefault());

  document.body.style.webkitUserSelect = 'none';
  document.body.style.userSelect = 'none';

  document.addEventListener('keydown', function(e) {
    if (e.ctrlKey && ['u','s','a'].includes(e.key)) { e.preventDefault(); return false; }
    if (e.key === 'F12') { e.preventDefault(); return false; }
    if (e.ctrlKey && e.shiftKey && ['I','J'].includes(e.key)) { e.preventDefault(); return false; }
    if (e.ctrlKey && e.key === 'c') {
      const tag = document.activeElement?.tagName?.toLowerCase();
      if (tag !== 'input' && tag !== 'textarea') { e.preventDefault(); return false; }
    }
  });

  document.querySelectorAll('img').forEach(img => {
    img.setAttribute('draggable', 'false');
    img.addEventListener('dragstart', e => e.preventDefault());
  });

  /* ─── START ON HOME ─── */
  switchPage('home');

});
